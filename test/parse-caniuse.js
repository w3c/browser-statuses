/**
 * Check Can I use parser
 */

import assert from 'assert';
import nock from 'nock';
import { fetchImplementationData, getImplementationStatus, getUserAgentsInfo } from '../src/parse-caniuse.js';

describe('The Can I Use parser', () => {
  before(async () => {
    nock.disableNetConnect();
    nock('https://caniuse.com')
      .get('/data.json')
      .reply(200, caniuseData);

    await fetchImplementationData();
    assert.ok(nock.isDone(), 'Parser did not fetch implementation data');
  });

  it('has some intel about Firefox versions', () => {
    const uaInfo = getUserAgentsInfo();
    const firefoxInfo = uaInfo.firefox;
    assert.ok(firefoxInfo, 'No version info for Firefox');
    assert.deepEqual(firefoxInfo, caniuseData.agents.firefox);
  });

  it('returns implementation info for the beacon spec', () => {
    const info = getImplementationStatus('beacon')[0];
    const expected = {
      ua: 'firefox',
      status: 'shipped',
      source: 'caniuse',
      href: 'https://caniuse.com/#feat=beacon',
      notes: [
        'A note on beacons in Firefox'
      ]
    };
    assert.deepEqual(info, expected);
  });
});


